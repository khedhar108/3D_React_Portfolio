/* this function is used to create a smooth animation effect for a container element that contains multiple child elements. The staggerChildren  and delayChildren properties allow for control over the timing of each child element's appearance.*/

export const staggerContainer = (staggerChildren, delayChildren) => {
  return {
    hidden: {}, // element will not be visible
    show: {
      transition: {
        // element visible with animation
        staggerChildren: staggerChildren,
        delayChildren: delayChildren || 0,
      },
    },
  };
};

